    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        if (this.controlador.buscarPasajero(Long.valueOf(this.txtBuscar.getText())) != null) {
            Pasajero p = this.controlador.buscarPasajero(Long.valueOf(this.txtBuscar.getText()));
            this.txtIdPasajero.setText(p.getIdPasajero().toString());
            this.txtNombre.setText(p.getNombre());
            this.txtApellido.setText(p.getApellido());
            this.txtTelefono.setText(p.getTelefono());
            this.txtEmail.setText(p.getEmail());
            this.dpFechaNac.setDate(p.getFnac());
            this.lblPasajero.setText(p.getNombre() + " " + p.getApellido());
            if (null != p.getComida()) {
                switch (p.getComida()) {
                    case "PASTA + CARNE":
                        this.rbPastaCarne.doClick();
                        break;
                    case "CARNE + ENSALADA":
                        this.rbCarneEnsalada.doClick();
                        break;
                    case "PASTA + SALSA":
                        this.rbPastaSalsa.doClick();
                        break;
                    case "ENSALADA":
                        this.rbEnsalada.doClick();
                        break;
                    default:
                        break;
                }
            }
            poblarTablaPasajerosItinerario(p);
        } else {
            int reply = JOptionPane.showConfirmDialog(null, "No existe pasajero con este id, desea agregar uno?", "", JOptionPane.YES_NO_OPTION);
            if (reply == JOptionPane.YES_OPTION) {
                limpiarPasajeros();
                this.txtNombre.requestFocus();
            } else {
                limpiarPasajeros();
                this.txtBuscar.requestFocus();
            }
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnDatosPasajero_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDatosPasajero_GuardarActionPerformed
        String id = this.txtIdPasajero.getText();
        String comida = null;

        if (this.rbCarneEnsalada.isSelected()) {
            comida = this.rbCarneEnsalada.getText();
        } else if (this.rbEnsalada.isSelected()) {
            comida = this.rbEnsalada.getText();
        } else if (this.rbPastaCarne.isSelected()) {
            comida = this.rbPastaCarne.getText();
        } else if (this.rbPastaSalsa.isSelected()) {
            comida = this.rbPastaSalsa.getText();
        } else {
            comida = "";
        }

        if ("".equals(id)) {
            this.controlador.agregarPasajero(false, comida, this.txtNombre.getText().toUpperCase(), this.txtApellido.getText().toUpperCase(),
                    this.txtTelefono.getText().toUpperCase(), this.txtEmail.getText().toUpperCase(), this.dpFechaNac.getDate());

        } else {
            Pasajero p = this.controlador.buscarPasajero(Long.valueOf(id));
            boolean vf = false;

            if (p.getReservas().size() < 10) {
                vf = false;
            } else if (p.getReservas().size() >= 10) {
                vf = true;
            }

            this.controlador.editarPasajero(p, vf, comida, this.txtNombre.getText().toUpperCase(), this.txtApellido.getText().toUpperCase(),
                    this.txtTelefono.getText().toUpperCase(), this.txtEmail.getText().toUpperCase(), this.dpFechaNac.getDate());
        }

        limpiarPasajeros();
        this.lblPasajero.setText("");

    }//GEN-LAST:event_btnDatosPasajero_GuardarActionPerformed

    private void btnReservarAsiento_GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarAsiento_GuardarActionPerformed
        String origen = this.txtOrigen.getText().toUpperCase();
        String destino = this.txtDestino.getText().toUpperCase();
        Date fecha = this.dpFecha.getDate();
        String fila = (String) this.AsientoFila.getValue();
        String columna = (String) this.AsientoColumna.getValue();

        Vuelo v = this.controlador.buscarODF(origen, destino, fecha);

        if (v == null) {
            JOptionPane.showMessageDialog(null, "Vuelo NO encontrado", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            boolean disponible = true;
            Vuelo vu = this.controlador.buscarVuelo(v.getIdVuelo());
            List<Reserva> reservas = vu.getReservas();
            for (Reserva r : reservas) {
                if (r.getFila().equals(fila)
                        && r.getColumna().equals(columna)) {
                    disponible = false;
                    break;
                }
            }

            if (disponible == false) {
                JOptionPane.showMessageDialog(null, "Asiento NO disponible", "Error", JOptionPane.ERROR_MESSAGE);
            } else {
                int precio;
                int f = Integer.valueOf(fila);
                if (f < 6 || f == 15 || f == 17) {
                    precio = v.getDistancia() * 2;
                } else {
                    precio = v.getDistancia();
                }
                if (this.txtIdPasajero.getText().isEmpty()) {
                    JOptionPane.showMessageDialog(null, "Debe buscar un Pasajero", "Error", JOptionPane.ERROR_MESSAGE);
                } else {
                    Pasajero p = this.controlador.buscarPasajero(Long.valueOf(this.txtIdPasajero.getText()));
                    this.controlador.agregarReserva(fila, columna, precio, v, p);
                    limpiarReservas();
                    poblarTablaPasajerosItinerario(p);
                }
            }
        }

    }//GEN-LAST:event_btnReservarAsiento_GuardarActionPerformed

