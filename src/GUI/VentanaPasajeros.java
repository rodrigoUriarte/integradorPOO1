    private void limpiarPasajeros() {
        this.txtBuscar.setText("");
        this.txtIdPasajero.setText("");
        this.txtNombre.setText("");
        this.txtApellido.setText("");
        this.txtTelefono.setText("");
        this.txtEmail.setText("");
        this.dpFechaNac.setDate(null);
        this.buttonGroup1.clearSelection();

        DefaultTableModel dtmPI = (DefaultTableModel) this.tbPasajerosItinerario.getModel();
        dtmPI.getDataVector().removeAllElements();
        dtmPI.fireTableDataChanged();

    }

    private void limpiarReservas() {
        this.txtOrigen.setText("");
        this.txtDestino.setText("");
        this.dpFecha.setDate(null);
        this.AsientoFila.getMinimumSize();
        this.AsientoColumna.getMinimumSize();
        this.txtTarifa.setText("");

    }

    public void poblarTablaPasajerosItinerario(Pasajero p) {
        Object[] nombreColumnas = {"idReserva", "Fecha", "Origen", "Destino"};
        DefaultTableModel dtmR = new DefaultTableModel(new Object[0][0], nombreColumnas);
        List<Reserva> reservas = p.getReservas();
        System.out.println(reservas.toString());
        for (Reserva reserva : reservas) {
            Object[] t = new Object[4];
            t[0] = reserva.getIdReserva();
            t[1] = reserva.getVuelo().getFechaSalida();
            t[2] = reserva.getVuelo().getOrigen();
            t[3] = reserva.getVuelo().getDestino();
            dtmR.addRow(t);
        }
        this.tbPasajerosItinerario.setModel(dtmR);
        this.tbPasajerosItinerario.clearSelection();
    }

